    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        String patientSelected = patientsComboBox.getItemAt(patientsComboBox.getSelectedIndex());
        patient = outputText.outputPatientDataText(patientSelected);

        dniTextField1.setText(patient.getDni());
        nameTextField.setText(patient.getName());
        surnameTextField1.setText(patient.getSurname());
        dobTextField1.setText(patient.getDob().toString());
        addressTextField1.setText(patient.getAddress());
        emailTextField1.setText(patient.getEmail());
        ageTextField1.setText(String.valueOf(patient.getAge()));
        sexTextField.setText(patient.getSexe());

        signalsComboBox.removeAllItems();

        File carpeta = new File("PatientsDB/" + patientSelected);
        String[] patientFicheros = carpeta.list();
        for (int i = 0; i < patientFicheros.length; i++) {
            if (patientFicheros[i].startsWith("signal")) {
                signalsComboBox.addItem(patientFicheros[i]);
            }
        }

        seeSignalButton.setVisible(true);

    }//GEN-LAST:event_okButtonActionPerformed

