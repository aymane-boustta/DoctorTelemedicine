    private void seeSignalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seeSignalButtonActionPerformed
        // TODO add your handling code here:
        String signalSelected = signalsComboBox.getItemAt(signalsComboBox.getSelectedIndex());
        ArrayList<Integer> arrayPulses = outputText.showBitalinoSignal(signalSelected);
        JFreeChart graph = null;
        DefaultCategoryDataset datos = new DefaultCategoryDataset();

        for (int i = 0; i < arrayPulses.size(); i++) {
            datos.addValue(arrayPulses.get(i), "Graph 1", String.valueOf(i));
        }
        graph = ChartFactory.createLineChart("ECG", "Time", "Action Potential", datos, PlotOrientation.VERTICAL, true, true, false);

        ChartPanel cPanel = new ChartPanel(graph);
        JFrame info = new JFrame("Graph");
        info.getContentPane().add(cPanel);
        info.pack();
        info.setVisible(true);
    }//GEN-LAST:event_seeSignalButtonActionPerformed

